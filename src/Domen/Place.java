package Domen;

public class Place {
    private int row;
    private int col;

    public Place(int x, int y) {
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }

    public boolean isEmpty() {
        return true;
    }
}

